	public static void loadingDataBase() throws InterruptedException 
	{
		int porcentaje=0;
		for (int i = 1;i<=8;i++) 
		{
			if(i==2) 
			{porcentaje=25;
			}
			if(i==5) 
			{
				porcentaje=75;
			}
			if(i==8) 
			{
				porcentaje=100;
			}
			ScreenUtilities.clearScreen();
			System.out.println("Loading DATA BASE.   Please wait ("+porcentaje+"% -).");
			Thread.sleep(250);
			
			ScreenUtilities.clearScreen();
			System.out.println("Loading DATA BASE..  Please wait ("+porcentaje+"% \\)..");
			Thread.sleep(250);


			ScreenUtilities.clearScreen();
			System.out.println("Loading DATA BASE... Please wait ("+porcentaje+"% |)...");
			Thread.sleep(250);
			
			ScreenUtilities.clearScreen();
			System.out.println("Loading DATA BASE.   Please wait ("+porcentaje+"% /)....");
			Thread.sleep(250);
			
			ScreenUtilities.clearScreen();
			System.out.println("Loading DATA BASE..  Please wait ("+porcentaje+"% -).....");
			Thread.sleep(250);
			
			ScreenUtilities.clearScreen();
			System.out.println("Loading DATA BASE... Please wait ("+porcentaje+"% \\)......");
			Thread.sleep(250);
			
			ScreenUtilities.clearScreen();
			System.out.println("Loading DATA BASE.   Please wait ("+porcentaje+"% |).");
			Thread.sleep(250);
			
			if(i==8) {
				System.out.println("           DATA-LOAD-DONE !!");
				Thread.sleep(3000);
			}
		}
	}
	
